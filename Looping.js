                                            //Looping Javascript


//Menggunakan While Looping


var n = 2;
while (n < 21) {
  console.log(n + 'kampang');

  n = n + 2;
}


var n = 20;
while (n > 0) {
  console.log(n + 'kampang');

  n = n - 2;
}


for(var n = 1; n < 10 ; n++){
  console.log(n + 'kampang');
}


for(var n = 10; n > 0 ; n--){
  console.log(n + 'kampang');
}
